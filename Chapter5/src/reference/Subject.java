public class Subject {
	//메서드 구현 
	String subjectName;
	int score;
	
	public void setSubjectName(String name) {
		subjectName = name;
		
	}

	public int getScore() {
		return score;
	}

	public void setScore(int score) {
		this.score = score;
	}

	public String getSubjectName() {
		return subjectName;
	}
	
	
	
	
}
